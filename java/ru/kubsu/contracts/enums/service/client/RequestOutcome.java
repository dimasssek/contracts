package ru.kubsu.contracts.enums.service.client;

/**
 * Итог обработки запроса по клиенту.
 */
public enum RequestOutcome {

    /** Ответ ещё не получен или обработка не завершена. */
    PENDING,

    /** Данные клиента обновлены по ответу ведомства. */
    UPDATED,

    /** Данные клиента уже актуальны, обновление не потребовалось. */
    ACTUAL,

    /** Ведомство не нашло запись по запросу. */
    NOT_FOUND,

    /** Ошибка при отправке или обработке запроса. */
    ERROR
}
